#include "../so_long.h"

int	right_car(char **tab)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (tab[i] != NULL)
	{
		while (tab[i][j] != '\0')
		{
			if (tab[i][j] != '0' && tab[i][j] != '1'
				&& tab[i][j] != 'C' && tab[i][j] != 'E'
				&& tab[i][j] != 'P')
				return (0);
			j++;
		}
		j = 0;
		i++;
	}
	return (1);
}
